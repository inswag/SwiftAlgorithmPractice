   $ 항상 작은 값을 위의 Node에 있게 해서 Root Node 에는 가장 작은 값이 오도록 하는 트리 구조가 Min Heap
  * Max Heap
   $ 항상 큰 값이 위의 Node에 있게 해서 Child Node는 Parents Node 보다 항상 작은 값을 가지도록 하는 트리 구조가 Max Heap
 - Min Heap 에 노드 삽입하기
